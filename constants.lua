  {Name="DVI_Video", Value=31, ButtonIndex=14},
}
function GetInputIndex(val)
  for i,input in ipairs(InputTypes) do
    if(input.Value == val)then
      return i
    end
  end
  for i,input in ipairs(AlternativeInputNames) do
    if(input.Value == val)then
      return input.ButtonIndex
    end
  end
end

